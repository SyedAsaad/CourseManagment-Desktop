    private void getAllDependentData() {
        addTableData();
    }

    private void addTableData() {
        List<Student> studentList = DbManager.getAllStudents(); 
        DefaultTableModel tableModel = (DefaultTableModel)studentTable.getModel();
        for(Student student : studentList){
            tableModel.addRow(new String[]{student.getName(),student.getCountry(),student.getPhoneNumber(),student.getCategory()});
        }
    }
    private void addTableData(Student student) { 
         DefaultTableModel tableModel = (DefaultTableModel)studentTable.getModel();
        tableModel.addRow(new String[]{student.getName(),student.getCountry(),student.getPhoneNumber(),student.getCategory()});
    }
    private void clearAllFields(){
        nameField.setText(null);
        phoneNumber.setText(null);
        countryField.setText(null);
        cityField.setText(null);
        dob.setDate(null);
        genderComboBox.setSelectedIndex(0);
        categoryComboBox.setSelectedIndex(0);
      
    }
