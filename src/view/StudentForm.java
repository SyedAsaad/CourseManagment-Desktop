    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(nameField.equals("") || countryField.equals("") || cityField.equals("") || phoneNumber.equals("") 
                || dob.equals("") || categoryComboBox.getSelectedItem().equals("Select Category")){
            JOptionPane.showMessageDialog(this, "Please Enter All Fields!");
        }
        else{
            Student student=new Student(nameField.getText(),categoryComboBox.getSelectedItem().toString(),phoneNumber.getText(),
                  countryField.getText(),cityField.getText(),dob.getDate().toString(),genderComboBox.getSelectedItem().toString());
          Boolean isSaved=DbManager.saveStudent(student);
                  if(isSaved){
                    JOptionPane.showMessageDialog(this, "Data Added!");
                    addTableData(student);
                    clearAllFields();
                  }
        }        
    }//GEN-LAST:event_jButton2ActionPerformed
    
    
