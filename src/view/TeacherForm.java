    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try{
            if(nameField.equals("") || qualificationField.equals("") || salaryField.equals("") || addrField.equals("") ){
                JOptionPane.showMessageDialog(this, "Please Enter All Fields!");
            }
            else{
                Teacher teacher=new Teacher(nameField.getText(),Long.parseLong(salaryField.getText()),qualificationField.getText(),addrField.getText());
                Boolean isSaved=DbManager.saveTeacher(teacher);
                      if(isSaved){
                        JOptionPane.showMessageDialog(this, "Data Added!");
                        addTableData(teacher);
                        clearAllFields();
                      }
            } 
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Salary must be number!");
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

