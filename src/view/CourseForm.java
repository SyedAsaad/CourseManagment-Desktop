    private void addTableData() {
     List<Course> courseList = DbManager.getAllCourse(); 
        DefaultTableModel tableModel = (DefaultTableModel)courseTable.getModel();
        for(Course course : courseList){
            tableModel.addRow(new String[]{course.getCourseName(),course.getTotalLectures().toString(),course.getCourseCategory()});
        }
    }

