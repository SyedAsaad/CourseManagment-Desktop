    private void addTableData(Teacher teacher) {
        DefaultTableModel tableModel = (DefaultTableModel)teacherTable.getModel();
        
            tableModel.addRow(new String[]{teacher.getName(),teacher.getQualification()});
        
    }

    private void getAllDependentData() {
        addTableData();
    }

    private void addTableData() {
       
        List<Teacher> teacherList = DbManager.getAllTeachers(); 
        DefaultTableModel tableModel = (DefaultTableModel)teacherTable.getModel();
        for(Teacher teacher : teacherList){
            tableModel.addRow(new String[]{teacher.getName(),teacher.getQualification()});
        }
        
    }
