    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try{
            if(courseField.equals("") || lectureField.equals("") || categoryComboBox.getSelectedItem().equals("Select Category")){
                JOptionPane.showMessageDialog(this, "Please Enter All Fields!");
            }
            else{
                Course course=new Course(courseField.getText(),Integer.parseInt(lectureField.getText()),categoryComboBox.getSelectedItem().toString());
                Boolean isSaved=DbManager.saveCourse(course);
                      if(isSaved){
                        JOptionPane.showMessageDialog(this, "Data Added!");
                        addTableData(course);
                        clearAllFields();
                      }
            } 
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Total Lecture must be number!");
        }
        catch(Exception e){
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

